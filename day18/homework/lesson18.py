# 3) მოიძიეთ ინფორმაცია % გაყოფის შესახებ, ახსენით კომენტარებით თუ რაში გვეხმარება ის და გააკეთეთ მაგალითები
# % (მოდულო) ოპერაციის ახსნა

# % აბრუნებს ნაშთს ორი რიცხვის გაყოფის შედეგად.
# მაგ:
# 17 % 5 → 17 გაყოფილია 5-ზე, ნაშთი არის 2, ამიტომ შედეგი: 2.

# სად გამოიყენება %

# ლუწი/კენტი რიცხვის განსაზღვრა
# x % 2 == 0 → ლუწი
# x % 2 == 1 → კენტი

# ციფრების ამოღება რიცხვიდან
# n % 10 → რიცხვის ბოლო ციფრი

# ციკლური გამოთვლები (მაგ., კვირის დღეები)
# day % 7

# დროის გარდაქმნა
# minutes % 60 → დარჩენილი წუთები

# ლუწი/კენტი რიცხვის შემოწმება
num = 11
if num % 2 == 0:
    print("ლუწია")
else:
    print("კენტია")
