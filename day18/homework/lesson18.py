#4) მომხმარებელს შემოატანინეთ რიცხვი და დაბეჭდეთ შემოტანილი რიცხვიდან ყველა გამყოფი, ანუ ისეთი რიცხვები რომლებზეც შემოტანილი რიცხვი უნაშთოდ იყოფა
n = int(input("შეიყვანეთ რიცხვი: "))

for i in range(1, n + 1):
    if n % i == 0:     # თუ ნაშთი 0-ია, გამყოფია
        print(i)
